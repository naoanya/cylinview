/*++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
 * Class definitions
 *----------------------------------------------------------------------
 */

class PseudoRand
{
public:
    uint32_t rand(void)
    {
        uint32_t t = (x_^(x_ << 11));
        x_ = y_; y_ = z_; z_ = w_;
        return (w_ = (w_^(w_ >> 19))^(t^(t >> 8)));
    }

    void setSeed(uint32_t x, uint32_t y, uint32_t z, uint32_t w)
    {
        x_ = x;
        y_ = y;
        z_ = z;
        w_ = w;
    }

    uint32_t x_ = 123456789;
    uint32_t y_ = 362436069;
    uint32_t z_ = 521288629;
    uint32_t w_ = 88675123;
};
